    public void fechaHora() {
        LocalDate now = LocalDate.now();
        Locale Regional = new Locale("es", "ES");
        Fecha_Hora.setText(now.format(DateTimeFormatter.ofPattern("EEEE dd 'de' MMMM 'del' YYYY", Regional)));
    }

